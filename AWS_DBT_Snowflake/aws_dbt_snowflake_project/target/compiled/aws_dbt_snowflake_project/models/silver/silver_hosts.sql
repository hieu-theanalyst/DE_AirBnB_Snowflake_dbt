

SELECT 
    HOST_ID,
    REPLACE (HOST_NAME, ' ', '_') as HOST_NAME,
    HOST_SINCE AS HOST_SINCE,
    IS_SUPERHOST AS IS_SUPERHOST,
    RESPONSE_RATE AS RESPONSE_RATE,
    CASE 
        WHEN RESPONSE_RATE = 100 THEN 'VERY GOOD'
        WHEN RESPONSE_RATE > 90 THEN 'GOOD'
        WHEN RESPONSE_RATE > 80 THEN 'FAIR'
        ELSE 'POOR'
    END AS RESPONSE_RATE_QUALITY,
    CREATED_AT AS CREATED_AT
FROM AIRBNB.bronze.bronze_hosts